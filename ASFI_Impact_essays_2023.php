                        <div class="text-center">
                            <h1 style="color:#e92a4e;">Attracted by Prospect of Winning Grants</h1>
                            <p>By Dr. Jerita Mwambi <br> Lecturer Pwani University, Kenya</p>
                        </div>
                     
                        <div class="text">
                            <p>One day, my colleague at the university forwarded to me a poster written “Research Funding and Grant Writing” course 
                                in block letters. The course was being organized by the African Science Frontiers Initiatives (ASFI). I had never come
                                 across ASFI before. I got interested in the course since my colleagues and I were already struggling with grant writing.
                                I still remember this day very well, precisely on 23rd March 2022. The deadline for the course application was 25th March.
                                I quickly sent in my application. I was very excited about this course and waited anxiously for the results of the application,
                                 which was to be announced on 1st April 2022. I was selected to participate in the course. 
                            </p>
                            <div class="row">
                                
                                <div class="col-lg-3">
                                    <img src="images/story1.jpg" width="100%" alt="">
                                </div>
                                <div class="col-lg-9">
                                    <p>
                                    The online Research Funding and Grant Writing held 15th - 19th April 2022 was my first ASFI course. That course week was a 
                                    very exciting and fulfilling one for me. I participated fully in every activity and gained much knowledge on how to develop 
                                    my grant idea and calve out a fundable grant proposal.  
                                    <br>
                                    As with every ASFI course, we were divided into groups, each group having the task of developing a funding proposal during the 
                                    course of the week. This hands-on, participatory approach was an eye-opener to me as it provided me with the opportunity to directly
                                    delve into putting into practice what I learned during the course. My course group provided me with the opportunity to interact and 
                                    network with many scholars from different African countries. 
                                    </p>
                                </div>
                            
                                <p>
                                A WhatsApp Group for all the course participants was established, allowing us to continue with the connections we established during the course.
                                 I joined the grant writing mentoring group established after the course. Through this platform, I have been able to collaborate and share productive
                                  ideas with other researchers from other disciplines and African countries. 
                                </p>

                                <p>
                                After the course, one of the participants sent a funding call to the WhatsApp Group. I quickly took up the call and organized three of my colleagues.
                                 I gained so much confidence after the ASFI course that I believed that we would be successful in grant call. The impact of the course on my confidence was remarkable. 
                                  My colleagues and I submitted the grant proposal. Out of 58 applicants, we were awarded the grant. This is just the beginning for me. This adds value to my career growth as a university lecturer.
                                </p>

                                <p>
                                I have continued to attend other ASFI courses, including Systematic Reviews and Meta-analysis and Art of Scientific Writing and Publishing, as well as the monthly 
                                science seminars and Dr. Nwaru’s Research Tips and Tricks, which are very enlightening and have continued to broaden my research horizon. ASFI has opened the doors of
                                 collaborations for me across the breadth and length of Africa. My attitude towards research has completely changed, I am now more enthusiastic about getting into more research work.
                                </p>

                                <p>
                                I have registered as an ASFI member and will continue to participate in upcoming courses and programs. I will continue to recommend the same to my colleagues.
                                 Thank you very much Dr Nwaru for coming up with such a great initiative.
                                </p>
                            </div>
                           
