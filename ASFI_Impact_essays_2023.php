                            <h1 style="color:#e92a4e;">My ASFI Journey</h1>
                            <p>By Mrs. Joana Oiza Adeleke, <br> Lecturer Bowen University, Nigeria</p>
                        </div>
                     
                        <div class="text">
                            <p>A journey of a thousand miles, they say, begins with a step. This is true for me as an academic. 
                                I started this journey a few years ago and I was in for a big surprise. A career in academics was not as I had
                                 imagined it to be. I had great lecturers during my undergraduate days, and I could not have 
                                imagined that academics will be so demanding. However, I courageously took on the challenge to be excellent. 
                            </p>
                            <div class="row">
                                <div class="col-lg-9">
                                <p>
                                I had heard about the African Science Frontiers Initiatives (ASFI) from a colleague at the workplace. She told me
                                 about a course that ASFI had organized, which she thought would be helpful to me. This was the beginning of my journey 
                                 with ASFI. I immediately joined the bandwagon. Since then, I have attended several ASFI monthly scientific seminars and
                                  joined one of the courses (systematic reviews and meta-analysis). These various encounters have made tremendous
                                   improvements in my teaching and research. I am now more passionate about passing across the knowledge and skills I
                                    am gaining to my students. My involvement with ASFI has not only been beneficial for my academic career but has 
                                    also been very impactful as an individual. 
                                </p>
                                </div>
                                <div class="col-lg-3">
                                    <img src="images/story1main.jpg" width="100%" alt="">
                                </div>
                                <p>
                                One area of improvement for me is with respect to critical reading and reviewing of scientific articles. A literature
                                 review is very essential in the research process. Although reading scientific articles is usually interesting for me,
                                  understanding several concepts in original papers can be difficult for me. This is particularly so for systematic review
                                   and meta-analysis papers. However, having participated in the last ASFI course on systematic reviews and meta-analysis,
                                    I saw a profound change in my abilities to critically appraise these papers. Now, I look forward to reading articles 
                                    on systematic reviews and meta-analysis because I have a better understanding of them.
                                </p>

                                <p>
                                Conducting research, excellent research, requires knowledge, research skills and a good attitude. The motivation is,
                                 through one’s research, to contribute positively to the development of the society. The effect excellent research has
                                  when implemented and applied to solving problems in the society is immeasurable. In doing this, the right mindset is
                                   indispensable. In all ASFI programs, there is a continuous emphasis on the central role of the society as the primary
                                    beneficiary of our research activities. This is a paradigm shift in my understanding of research and thus has led to a
                                     great improvement in my mindset towards research and its wider societal impact. I am now more eager to not just 
                                     conduct research but also to ensure that my research has an impact in the society. This and more are some of the 
                                     things ASFI encourages and emphasizes. 
                                </p>

                                <p>
                                My journey in ASFI continues. My decision is that ASFI will continue to be my companion in my career as an academic and 
                                researcher. Learning is continuous and never stops. I look forward to participating in more seminars, courses and programs
                                 organized by ASFI.
                                </p>
                            </div>
                           
