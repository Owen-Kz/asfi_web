                        <div class="text-center">
                            <h1 style="color:#e92a4e;">When Persistence Pays Off</h1>
                            <p>Dr. Precious Eteike, Senior Resident Doctor <br> Federal University Teaching Hospital Owerri, Nigeria</p>
                        </div>
                     
                        <div class="text">
                            <p>My story with the African Science Frontiers Initiatives (ASFI) began in June 2021. A colleague of mine had sent me a link for
                                 application to the 2021 edition of the Systematic Review and Meta-Analysis course. With a great enthusiasm and positive
                                  expectations, I quickly sent in my application. Unfortunately, on 9th July 2021, I got the disappointing, negative, and 
                                  mood-changing e-mail from ASFI that read, “I regret to say that you have not been accepted to this edition of the course”. 
                                  What a sad moment it was for me! But I accepted the decision in good fate and moved on, yet determined to try again next time. 
                            </p>
                            <p>
                                I attended the one-year anniversary of ASFI themed, “Celebrating a little beginning, looking into big future” held in July 2021. 
                                This event was an eye opener and quite enlightening. From that event, I decided to pay close attention to the programs of ASFI and 
                                not be discouraged. This was a herculean task as the timing of some of the programs would subsequently clash with my work schedules, but I didn’t give up. 
                            <br>
                                Next in line in ASFI programs was the tenth edition of the Seminar Series 2021 titled “Playing the game, the unwritten rules of a 
                                successful scientist” This served as a booster and catalyst and thus helped in no small measure to keep my hope alive. I have also attended
                                 many editions of the “Dr Nwaru’s Research Tips and Tricks” from which I have learnt a lot. 

                            </p>
                            <div class="row">
                                <div class="col-lg-9">
                                <p>
                                The time came to apply for the 2022 edition of the Systematic Reviews and Meta-Analysis. This was exactly one year when my 2021 application
                                 was denied. Precisely, on the 10th of July 2022, I got an e-mail from ASFI with joy that I had been accepted to participate in the course. 
                                 The news was so encouraging to me to say the least. I made the necessary preparations by taking a casual leave from work. Indeed, words will
                                  certainly not describe my experience during this course. The excellent organization, rich content, and exceptional teaching approach remain
                                   enduring in my heart. The break-out and hands-on-sessions were so impactful. 
                                </p>
                                </div>
                                <div class="col-lg-3">
                                    <img src="images/junior1.jpg" width="100%" alt="">
                                </div>
                                <p>
                                I was also accepted to the Art of Scientific Writing and Publishing course, which took place in December 2022. It was after this course that I 
                                had no option than to register as a member of ASFI when the call was made. I am currently undergoing the next course “Research: Quality, Productivity and Impact.”
                                </p>

                                <p>
                                Using the skills learned from these courses, I have now published three original articles in international journals; established a functional, multi-disciplinary/specialties
                                 research collaboration team in the Hospital where I work. I am currently collaborating with colleagues to produce a systemic review on polycystic ovarian syndrome in African women.
                                  For these, I am eternally grateful to the ASFI.
                                </p>

                                <p>
                                No quality societal development can occur without quality scientific research and development. This I have learnt from ASFI. Indeed, my journey so far with ASFI has been an impactful and a transformative one. 
                                </p>
                            </div>
                           
