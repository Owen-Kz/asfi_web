                        <div class="text"> The platform is expected to re-invent the learning experience by encouraging result-focused collaboration between communities, researchers, industries across borders and cultures. It is also aimed at offering educational resources to all African scholars without borders. Experts from various fields of endeavour will be invited to contribute and share contents within the platform for the benefit of scientists in the continent.
                             AFSIScholar can be seen as the African version of Coursera or other related e-Learning platforms, but it is solely dedicated to Africans, with additional aspects of networking and collaboration among Africans for the sake of generating connected projects that will spur development of the continent. </div>
                            
