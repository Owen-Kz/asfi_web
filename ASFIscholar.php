                        <div class="text">The proposed ASFIScholar is an e-Learning platform that incorporates some attractions of the social media, including blogging, chatting, mailing, podcasting, as well as video and teleconferencing to enable African academics, students, industry experts and employers to share resources, learn across borders, connect socially, and collaborate on impact-focused projects across the continent. </div>
