                                    of belonging and that are in tandem with ASFI's vision and mission - that is, continuous initiation of programs that 
                                    culminate in the capacity building and career development of African scientists. The General Assembly Leadership 
                                    Team meets every month.
                                </p>

                                <p>
                                Membership in the General Assembly Leadership Team is a voluntary role and will be based on election and voted by ASFI members.
                                 The Leadership Team will be rotated across African regions, and will take into account gender, disciplines, career level,
                                  age, experience, and other parameters that will ensure full representation of the different sub-groups represented in
                                   the General Assembly and to facilitate fruitful engagements and growth of the General Assembly. Tenure for each 
                                   Leadership Team will be non-renewal 2 years. Each member of the Leadership Team who performs and lives up to the 
                                   required and expected role of his/her office will receive a Community Service certificate from ASFI at the end of their
                                    tenure.
