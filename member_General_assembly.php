                            different career levels.
                        </p>
                        
                        <p>
                            The leaders of the Senior and Junior Sub-Assemblies, taking into account the career stage of each ASFI member,
                            have the overarching goals of advancing capacity building, career development, networking, mentoring and collaboration
                            among the members of the respective sub-Assemblies.
                        </p>

                        <p>
                            The Chairs and Vice Chairs of the Junior and Senior Sub-Assemblies work with members of the sub-assembly to initiate, 
                            develop and run its own capacity building, career development, networking, and collaboration programs for the maximum benefit of members.
                        </p>

                        <p>
                            The leaders work with members to the sub-assemblies to develop the rules of engagements for the sub-assemblies to ensure smooth running of their activities.
                        </p>

                        <p>
                        The leaders explore the possibility of organizing events between the Junior and Senior sub-assemblies for collaboration and mentorship between members of the sub-assemblies.
                        </p>

                        <p>
                        The Junior and Senior sub-assemblies report their activities to the General Assembly Leadership Team every 6 months.
                        </p>

                        <p>
                        Each planned activity is approved by ASFI leadership before commencing the program.
                        </p>

                        <p>
                        The Chairs and Vice Chairs of the Junior and Senior Sub-Assemblies shall have a 2-year tenure. The Chairs of the Junior and Senior Sub-Assemblies are members of the ASFI Members'
