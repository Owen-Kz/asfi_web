                        <h3>Our History & Background </h3>
                        <div class="text">Our continent, Africa, lags behind in most, if not all, indicators of modern societal development. Current trajectories barely give any indication for future hope. Hunger thrives, security worsens, quality of education remains poor, electricity continues to dilapidated level in most countries, clean water continues to elude us, just to name a few. The African population continues to grow, projected to nearly surpass that of Asia by 2100. The developmental frameworks to cater for such population surge are currently not in place and there is no indication that reliable plans are being put in place. Nevertheless, the potentials are there to make Africa a key global player in many aspects.</div><br>
                        <div class="text">One sector of the society that has been severely neglected in the developmental discourse of Africa is science and academia. According to the United Nations Educational, Scientific and Cultural Organization (UNESCO), the ultimate goal of science is to generate “solutions for everyday life”. Through this science ensures “a longer and healthier life, monitors our health, provides medicine to cure our diseases, alleviates aches and pains, helps us to provide water for our basic needs – including our food, provides energy and makes life more fun, including sports, music, entertainment and the latest communication technology.” Without value-oriented scientific research, well-intentioned, with vision to solve problems, no nation can be built. Clearly, there can be no meaningful societal development a thriving science. However, science can only thrive in any nation if only scientists have the right competencies, capacity, and state-of-the-art knowledge-base and skills. This is the overarching motivation for ASFI: to contribute to African science that tackles Africa’s developmental challenges. We believe that only a competent science can change our current narratives.</div>

